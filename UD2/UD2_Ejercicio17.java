public class UD2_Ejercicio17 {

	public static void main(String[] args) {

		/*
		 * Crear un programa que muestre los números pares del 1 al 100 utilizando bucle
		 * while
		 */

		int i = 0;

		System.out.println("Voy a mostrar todos los números pares entre 1 y 100");
		
		while (i < 100) {
			i = i + 2;
			System.out.println(i);
		}
	}

}
