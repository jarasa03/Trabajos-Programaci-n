public class UD2_Ejercicio16 {

	public static void main(String[] args) {

		/*
		 * Crear un programa que muestre los números pares del 1 al 100 utilizando bucle
		 * for
		 */

		System.out.println("Voy a mostrar todos los números pares del 1 al 100");
		
		for (int i = 0;(i>=0) && (i<=100);i=i+2) {
			System.out.println(i);
		}
	}

}
